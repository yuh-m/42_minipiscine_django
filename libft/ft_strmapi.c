#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	cnt;
	char	*result;

	if (!s || !f)
		return (NULL);
	result = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!result)
		return (NULL);
	cnt = 0;
	while (s[cnt])
	{
		result[cnt] = f(cnt, s[cnt]);
		cnt++;
	}
	result[cnt] = '\0';
	return (result);
}
